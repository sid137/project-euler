#include <iostream>
#include <vector>
#include <cctype>
#include <string>
#include <cstdio>
#include <cstdlib>

using std::cout;
using std::cin;
using std::endl;
using std::vector;
using std::string;

int main() {

	string number, line;
	while (cin >> line)
		number += line;

	int n;
	for (string::size_type i = 0; i != number.length(); ++i) {
		cout << n*1;
	}

	cout << endl;
	cout << endl;
	cout << endl;
	cout << number << endl;
	return 0;
}
